def even_sum(limit):
    # 라인 9: 값을 변수에 저장하거나 갱신해 이후 로직에서 재사용합니다.
    total = 0
    # 라인 10: 반복 가능한 데이터를 순회하며 같은 작업을 반복합니다.
    for n in range(1, limit + 1):
        # 라인 11: 조건을 평가해 실행 흐름을 분기하는 조건문입니다.
        if n % 2 == 0:
            # 라인 12: 값을 변수에 저장하거나 갱신해 이후 로직에서 재사용합니다.
            total += n
    # 라인 13: 함수 실행 결과를 호출한 위치로 반환하고 함수 실행을 종료합니다.
