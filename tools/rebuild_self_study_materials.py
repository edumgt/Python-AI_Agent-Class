def render_mermaid_flowchart(steps: list[str]) -> str:
    lines = ["flowchart TD"]
    for idx, step in enumerate(steps, start=1):
        lines.append(f'    N{idx}["{sanitize_mermaid_label(step)}"]')
    for idx in range(1, len(steps)):
        lines.append(f"    N{idx} --> N{idx + 1}")
    return "\n".join(lines)


