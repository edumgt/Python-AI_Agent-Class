def class_dir_from_row(row: dict[str, str]) -> Path:
    md_rel = (row.get("md_file") or "").strip()
    if md_rel:
        md_path = ROOT / Path(md_rel)
        if md_path.name:
            return md_path.parent
    return ROOT / row["class"]


def class_rel_dir_from_row(row: dict[str, str]) -> str:
    md_rel = (row.get("md_file") or "").strip()
    if md_rel:
        return Path(md_rel).parent.as_posix()
    return row["class"]


def subject_root_from_row(row: dict[str, str]) -> str:
    rel_dir = class_rel_dir_from_row(row)
    return rel_dir.split("/", maxsplit=1)[0] if rel_dir else ""


def filter_rows_by_subject_roots(rows: list[dict[str, str]], roots: set[str]) -> list[dict[str, str]]:
    if not roots:
        return rows
    return [row for row in rows if subject_root_from_row(row) in roots]


def build_self_study_materials(roots: set[str] | None = None) -> None:
    roots = roots or set()
    if not INDEX_FILE.exists():
        raise FileNotFoundError(f"Cannot find index file: {INDEX_FILE}")

    with INDEX_FILE.open(encoding="utf-8-sig", newline="") as fp:
        lines = [line for line in fp if line.strip() and not line.lstrip().startswith("#")]
        raw_rows = list(csv.DictReader(lines))

    rows: list[dict[str, str]] = []
    for raw in raw_rows:
        cleaned = {str(key).lstrip("\ufeff"): value for key, value in raw.items()}
        rows.append(cleaned)

    rows = filter_rows_by_subject_roots(rows, roots)
    if not rows:
        print("Updated 0 class markdown files.")
        return

    def class_number(class_id: str) -> int:
        match = re.search(r"(\d+)$", class_id)
        if not match:
            return 10**9
        number = int(match.group(1))
        if class_id.startswith("project"):
            # Place project001~project020 after class500.
            return 500 + number
        return number

    ordered_rows = sorted(rows, key=lambda r: class_number(r["class"]))
    neighbors: dict[str, tuple[dict[str, str] | None, dict[str, str] | None]] = {}
    for i, current in enumerate(ordered_rows):
        prev_row = ordered_rows[i - 1] if i > 0 else None
        next_row = ordered_rows[i + 1] if i + 1 < len(ordered_rows) else None
        neighbors[current["class"]] = (prev_row, next_row)

    for row in ordered_rows:
        class_id = row["class"]
        module = row["module"]
        subject_name = row["subject_name"]
        track = choose_track(subject_name, module)
        prev_row, next_row = neighbors[class_id]

        class_dir = class_dir_from_row(row)
        class_rel_dir = class_rel_dir_from_row(row)
        class_dir.mkdir(parents=True, exist_ok=True)
        md_path = class_dir / f"{class_id}.md"
        example_path = class_dir / f"{class_id}_example1.py"
        quiz_path = class_dir / f"{class_id}_quiz.html"

        flow_steps = build_flow_steps(
            row=row,
            track=track,
            class_rel_dir=class_rel_dir,
            example_file=example_path.name,
            next_row=next_row,
        )
        flow_mermaid = render_mermaid_flowchart(flow_steps)
        flow_image_file = f"{class_id}_flow.png"
        save_flow_png(
            steps=flow_steps,
            out_path=class_dir / flow_image_file,
            class_id=class_id,
            module=module,
        )
        tech_stack_block = render_auto_tech_stack_block(
            class_id=class_id,
            class_rel_dir=class_rel_dir,
            subject_name=subject_name,
            module=module,
            example_file=example_path.name,
            track=track,
            mermaid_flow=flow_mermaid,
            flow_image_file=flow_image_file,
        )

        md_path.write_text(
            render_markdown(
                row=row,
                track=track,
                class_rel_dir=class_rel_dir,
                example_file=example_path.name,
                quiz_file=quiz_path.name,
                prev_row=prev_row,
                next_row=next_row,
                tech_stack_block=tech_stack_block,
            ),
            encoding="utf-8",
            newline="\n",
        )
        if not example_path.exists():
            example_path.write_text(
                render_example(track=track, class_id=class_id, module=module),
                encoding="utf-8",
                newline="\n",
            )

    print(f"Updated {len(ordered_rows)} class markdown files.")
    if roots:
        print(f"Subject roots: {', '.join(sorted(roots))}")


if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Rebuild self-study markdown and flow assets.")
    parser.add_argument(
        "--subject-root",
        action="append",
        default=[],
        help="Rebuild only rows under this root folder (e.g. dataVizPrep). Can be repeated.",
    )
    args = parser.parse_args()
    roots = {item.strip() for item in args.subject_root if item.strip()}
    build_self_study_materials(roots=roots)
